    <h1>Questions</h1>

    <p>Q: What is Version Control Software?</p>
    <p>A: It allows for changes to be saved individualy, allowing you to revert to a previous save in case something stops working after a change.</p>

    <p>Q: What is Add?</p>
    <p>A: Add primes git that the changes were intended.</p>

    <p>Q: What is Commit?</p>
    <p>A: Commit confirms those changes and saves them localy.</p>

    <p>Q: What is Push?</p>
    <p>A: Push send the commit to the server.</p>

    <p>Q: What is Pull?</p>
    <p>A: Pull will update your local repository with any updates from the remote repository.</p>

    <p>Q: What is Clone?</p>
    <p>A: Clone will create a version of the repository on the server that it is run.</p>
    
